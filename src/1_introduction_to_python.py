# Part 1: Introduction to Python

# 1. Brief History of Python
# Python was created by Guido van Rossum and first released in 1991.
# It is known for its readability, simplicity, and versatility.
# Python 2 and Python 3 are the major versions, with Python 3 being the latest and recommended version.

# 2. Installation of Python
# Visit the official Python website: https://www.python.org/
# Download and install the latest version of Python for your operating system.
# During installation, make sure to check the option to add Python to your PATH.

# 3. Setting up a Development Environment
# You can use various Integrated Development Environments (IDEs) for Python.
# Examples include Visual Studio Code, PyCharm, Jupyter Notebooks, etc.

# For this example, we'll use a simple text editor and the command line.

# Create a new file called "hello_world.py" and add the following code:
print("Hello, world!")

# Save the file.

# Open a command prompt or terminal and navigate to the directory where your "hello_world.py" file is located.

# Run the script by typing:
# python hello_world.py
# or
# python3 hello_world.py  # on some systems

# You should see the output: "Hello, world!"

# Congratulations! You've successfully set up Python and executed your first script.
